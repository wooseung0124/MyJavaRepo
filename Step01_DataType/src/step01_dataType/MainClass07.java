package step01_dataType;

public class MainClass07 {
	public static void main(String[] args) {
		//지역 변수를 미리 만들고 나중에 필요한 값을 넣고 싶으면 초기값을 대입하는 것이 좋다.
		
		int num1 = 0; //숫자 type의 초기값은 0을 넣으면 된다.
		String name1 = null; // 참조 type의 초기값은 null을 넣으면 된다.
		boolean boolean1 = false; // boolean type의 초기값은 false로 넣으면 된다.
		
		num1=10;
		name1="김구라";
		boolean1=true;
	}
}
